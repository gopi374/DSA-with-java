class prettyprinting{
    public static void main(String[] args) {
        float a = 15762.879f;
        System.out.printf("formating num %.2f",a);
        System.out.println();

        System.out.printf("int num %i",2598);

        //strimg
        System.out.printf("hello i am %s,i am %s year student","gopi","second");
        System.out.println();
        //character
        System.out.printf("characters at index[0] %c",'C');
        System.out.println();
        //
        System.out.printf("pi value %f",3.14f);
    }
}